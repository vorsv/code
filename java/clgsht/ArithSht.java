
public class ArithSht{
    public static void main(String[] args){
        int z=2,y=1;
        System.out.println("difference of two int : " + (z-y));
        System.out.println("adding two int : " + (z+y));
        System.out.println("prod of two int : " + (z*y));
        System.out.println("division of two int : " + (z/y));
        System.out.println("modulo of two int : " + (z%y));
    }
}